package Domain;

/**
 *
 * @author fabian
 */
public class Table {
    
    private int ID;
    private Order order;
    private String status;

    public Table() {
    }

    public Table(int ID, Order order, String status) {
        this.ID = ID;
        this.order = order;
        this.status = status;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    
     
    
}
